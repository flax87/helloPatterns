package com.company;

public class Rectangle {

    private int id;
    private int length;
    private int width;
    public final double PI = 3.14;

//Constructor
    public Rectangle(int id, int length, int width) {
        this.id = id;
        this.length = length;
        this.width = width;
    }

    public Rectangle() {

    }


    //Get & Set
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public double getPI() {
        return PI;
    }







        




    }

